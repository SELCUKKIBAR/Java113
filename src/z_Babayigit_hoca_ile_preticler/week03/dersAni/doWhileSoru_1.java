package z_Babayigit_hoca_ile_preticler.week03.dersAni;

public class doWhileSoru_1 {
    public static void main(String[] args) {
        /*
        'k' harfinden 't'
        harfine kadar harfleri yazdirin.
         */

        char ilkHrf = 'k';


        do {

            System.out.print(ilkHrf+"-");
            ilkHrf++;

        } while (ilkHrf<='t');


    }
}
